package org.gwaspi.model;

import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import org.gwaspi.constants.cNetCDF.Defaults.OPType;

/**
 *
 * @author Fernando Mu√±iz Fernandez
 * IBE, Institute of Evolutionary Biology (UPF-CSIC)
 * CEXS-UPF-PRBB
 */
@Entity
@Table(name = "report")
@NamedQueries({
	@NamedQuery(
		name = "report_fetchById",
		query = "SELECT r FROM Report r WHERE r.id = :id"),
	@NamedQuery(
		name = "report_fetchByParentOperationIdParentMatrixId",
		query = "SELECT r FROM Report r WHERE r.parentOperationId = :parentOperationId AND r.parentMatrixId = :parentMatrixId"),
	@NamedQuery(
		name = "report_deleteByParentMatrixId",
		query = "DELETE FROM Report r WHERE r.parentMatrixId = :parentMatrixId"),
	@NamedQuery(
		name = "report_deleteByParentOperationId",
		query = "DELETE FROM Report r WHERE r.parentOperationId = :parentOperationId"),
})
public class Report implements Serializable {

	private int id; // INTEGER generated by default as identity
	private String friendlyName; // VARCHAR(255) NOT NULL
	private String fileName; // VARCHAR(255) NOT NULL
	private OPType type; // VARCHAR(32) NOT NULL
	private int parentMatrixId; // INTEGER
	private int parentOpId; // INTEGER
	private String description; // VARCHAR(255)
	private int studyId; // INTEGER

	protected Report() {

		this.id = Integer.MIN_VALUE;
		this.friendlyName = "";
		this.fileName = "";
		this.type = null;
		this.parentMatrixId = Integer.MIN_VALUE;
		this.parentOpId = Integer.MIN_VALUE;
		this.description = "";
		this.studyId = Integer.MIN_VALUE;
	}

	public Report(
			int id,
			String friendlyName,
			String fileName,
			OPType type,
			int parentMatrixId,
			int parentOpId,
			String description,
			int studyId)
	{
		this.id = id;
		this.friendlyName = friendlyName;
		this.fileName = fileName;
		this.type = type;
		this.parentMatrixId = parentMatrixId;
		this.parentOpId = parentOpId;
		this.description = description;
		this.studyId = studyId;
	}

	/**
	 * Returns the unique report identification number.
	 * @return the report-id
	 */
	@Id
	@GeneratedValue
	@Column(
		name       = "id",
		unique     = false,
		nullable   = false,
		insertable = true,
		updatable  = false
		)
	public int getId() {
		return id;
	}

//	/**
//	 * Sets the unique report identification number.
//	 * @param id the report-id to set
//	 */
	protected void setId(int id) {
		this.id = id;
	}

	@Id
	@Column(
		name       = "parentMatrixId",
		unique     = false,
		nullable   = false,
		insertable = true,
		updatable  = false
		)
	public int getParentMatrixId() {
		return parentMatrixId;
	}

	protected void setParentMatrixId(int parentMatrixId) {
		this.parentMatrixId = parentMatrixId;
	}

	@Id
	@Column(
		name       = "studyId",
		unique     = false,
		nullable   = false,
		insertable = true,
		updatable  = false
		)
	public int getStudyId() {
		return studyId;
	}

	protected void setStudyId(int studyId) {
		this.studyId = studyId;
	}

	@Column(
		name       = "friendlyName",
		unique     = false,
		nullable   = false,
		insertable = true,
		updatable  = false
		)
	public String getFriendlyName() {
		return friendlyName;
	}

	protected void setFriendlyName(String friendlyName) {
		this.friendlyName = friendlyName;
	}

	@Column(
		name       = "fileName",
		unique     = false,
		nullable   = false,
		insertable = true,
		updatable  = false
		)
	public String getFileName() {
		return fileName;
	}

	protected void setFileName(String fileName) {
		this.fileName = fileName;
	}

	@Column(
		name       = "type",
		unique     = false,
		nullable   = false,
		insertable = true,
		updatable  = false
		)
	public OPType getReportType() {
		return type;
	}

	protected void setReportType(OPType type) {
		this.type = type;
	}

	@Id
	@Column(
		name       = "parentOpId",
		unique     = false,
		nullable   = false,
		insertable = true,
		updatable  = false
		)
	public int getParentOperationId() {
		return parentOpId;
	}

	protected void setParentOperationId(int parentOpId) {
		this.parentOpId = parentOpId;
	}

	@Column(
		name       = "description",
		unique     = false,
		nullable   = false,
		insertable = true,
		updatable  = false
		)
	public String getDescription() {
		return description;
	}

	protected void setDescription(String description) {
		this.description = description;
	}
}
